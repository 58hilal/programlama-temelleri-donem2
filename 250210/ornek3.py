tek_sayilar=[3,5,7,9,11,13,15,17,19]
print(tek_sayilar[0:6])                 # 0'dan 6.indekse kadar yazdır
print(tek_sayilar[2:5])                 # 2. indeksden 5. indeke kadar olan elemanları yazdır
print(tek_sayilar[3:8])                 # 3. indeksden 8. indekse kadar olan elemanları yazdır
print(tek_sayilar[:5])                  # listenin başından itibaren 5. indekse kadar ki elemanları yazdır
print(tek_sayilar[3:])                  # 3. indeksten sona kadar tüm liste elemanlarını yazdır
print(tek_sayilar[0:8:2])               # 0'dan 8. indekse kadar 2şer 2şer yazdır
print(tek_sayilar[::3])                 # 0'dan 3er 3er artarak yazdır